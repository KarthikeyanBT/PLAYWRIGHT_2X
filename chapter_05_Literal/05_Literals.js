// Literals in JavaScript

// String Literals
console.log('Single quotes string');
console.log("Double quotes string");
console.log(`Template literal with expression: ${2 + 3}`);

// Numeric Literals
console.log(42);        // Integer
console.log(3.14159);   // Floating point
console.log(1e5);       // Exponential (100000)
console.log(0xFF);      // Hexadecimal (255)

// Boolean Literals
console.log(true);
console.log(false);

// Special Literals
console.log(null);
console.log(undefined);

// Object Literal
let person = { name: "Bob", age: 30 };
console.log(person);

// Array Literal
let colors = ["red", "green", "blue"];
console.log(colors);